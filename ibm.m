(** Hamiltonian matrix for one operator **)
hopsqb[ls_,Nb_,J_,hop_]:=
Block[{bn,dn,c2},
bn=basb[ls,Nb,J]; dn=Length[bn];
If[hop===c22su3sd,
   c2=hopsqb[ls,Nb,J,c2su3sd]; c2 . c2,
   Table[hop[ls,bn[[ib]],bn[[ik]]],{ib,1,dn},{ik,1,dn}]]];
hopb[ls_,Nb_,J_,hop_]:=
(*hopb[ls,Nb,J,hop]=*)
Block[{bn,dn,c2,c22},
bn=basb[ls,Nb,J]; dn=Length[bn];
If[hop===c22su3sd,
   c2=hopb[ls,Nb,J,c2su3sd]; c22=sqh[c2] . sqh[c2];
   Flatten[Table[c22[[ib,ik]],{ib,1,dn},{ik,ib,dn}],1],
   Flatten[Table[hop[ls,bn[[ib]],bn[[ik]]],{ib,1,dn},{ik,ib,dn}],1]]];
hopb[ls_,ps_,Nb_,J_,P_,hop_]:=
(*hopb[ls,ps,Nb,J,P,hop]=*)
Block[{bn,dn},
bn=basb[ls,ps,Nb,J,P]; dn=Length[bn];
Flatten[Table[hop[ls,bn[[ib]],bn[[ik]]],{ib,1,dn},{ik,ib,dn}],1]];

(** Hamiltonian matrix for a combination of operators and interactions **)
(* hops is an array {{cf1,operator1},...} 
   where cfi is a coefficient in front of operatori
   ints is an array {{cf1,int1},...} *)
(* With multipole-to-normal-order conversion *)
hopvb1[ls_,Nb_,J_,hops_,ints_]:=
Block[{kmax,conv},
kmax=Max[Map[order,hops[[All,2]]]];
conv=multonorb[ls,hops];
Sum[hamb[ls,k,Nb,J],{k,0,kmax}]/.conv]+
Sum[ints[[i,1]]*hovb[ls,Nb,J,ints[[i,2]]],{i,1,Length[ints]}];
hopvb1[ls_,ps_,Nb_,J_,P_,hops_,ints_]:=
Block[{kmax,conv},
kmax=Max[Map[order,hops[[All,2]]]];
conv=multonorb[ls,hops];
Sum[hamb[ls,ps,k,Nb,J,P],{k,0,kmax}]/.conv]+
Sum[ints[[i,1]]*hovb[ls,ps,Nb,J,P,ints[[i,2]]],{i,1,Length[ints]}];
(* Without multipole-to-normal-order conversion *)
hopvb2[ls_,Nb_,J_,hops_,ints_]:=
Sum[hops[[i,1]]*hopb[ls,Nb,J,hops[[i,2]]],{i,1,Length[hops]}]+
Sum[ints[[i,1]]*hovb[ls,Nb,J,ints[[i,2]]],{i,1,Length[ints]}];
hopvb2[ls_,ps_,Nb_,J_,P_,hops_,ints_]:=
Sum[hops[[i,1]]*hopb[ls,ps,Nb,J,P,hops[[i,2]]],{i,1,Length[hops]}]+
Sum[ints[[i,1]]*hovb[ls,ps,Nb,J,P,ints[[i,2]]],{i,1,Length[ints]}];
(* Choose one as the default *)
hopvb[ls_,Nb_,J_,hops_,ints_]:=hopvb1[ls,Nb,J,hops,ints];
hopvb[ls_,ps_,Nb_,J_,P_,hops_,ints_]:=hopvb1[ls,ps,Nb,J,P,hops,ints];

(** Spectrum of a linear combination of multipole operators and interactions **)
(* n eigenstates for the angular momenta in Js.
   c=1: prints n eigenvalues relative to the J=0 ground state
   c=2: + eigenvector of yrast states
   c=3: + n eigenvectors
   c=4: outputs n eigenvalues and eigenvectors *)
specb[ls_,Nb_,Js_,n_,hops_,ints_,c_]:=
Block[{tab,J,ham,eig,egs,dim,nd},
egs=0;
tab=Table[J=Js[[i]];
          ham=sqh[hopvb[ls,Nb,J,hops,ints]];
          dim=Length[ham]; nd=Min[n,dim];
          If[dim==0,{},
             eig=Map[Take[#,nd]&,eigensystem[ham]];
             If[J==0,egs=eig[[1,1]]; Print["Ground-state energy: ",egs]];
             If[c==1||c==2||c==3,Print["J=",J," (dim=",dim,") Energies: ",Take[eig[[1]]-egs,nd]]];
             If[c==2,Print["Eigenvector yrast state: ",eig[[2,1]]]];
             If[c==3,Do[Print["Eigenvector state ",j,": ",eig[[2,j]]],{j,1,nd}]];
             If[c==4,Print["J=",J," (dim=",dim,") Energies: ",Take[eig[[1]],nd]]];
             eig],
          {i,1,Length[Js]}];
If[c==4,tab]];
specb[ls_,ps_,Nb_,Js_,Ps_,n_,hops_,ints_,c_]:=
Block[{tab,J,P,sP,ham,eig,egs,dim,nd},
egs=0;
tab=Table[J=Js[[i]]; P=Ps[[i]]; sP=Which[P==1,"+",P==-1,"-"];
          ham=sqh[hopvb[ls,ps,Nb,J,P,hops,ints]];
          dim=Length[ham]; nd=Min[n,dim];
          If[dim==0,{},
             eig=Map[Take[#,nd]&,eigensystem[ham]];
             If[J==0 && P==1,egs=eig[[1,1]]; Print["Ground-state energy: ",egs]];
             If[c==1||c==2||c==3,Print["J=",J,sP," (dim=",dim,") Energies: ",Take[eig[[1]]-egs,nd]]];
             If[c==2,Print["Eigenvector yrast state: ",eig[[2,1]]]];
             If[c==3,Do[Print["Eigenvector state ",j,": ",eig[[2,j]]],{j,1,nd}]];
             If[c==4,Print["J=",J,sP," (dim=",dim,") Energies: ",Take[eig[[1]],nd]]];
             eig],
   {i,1,Length[Js]}];
If[c==4,tab]];

(** Transition matrices **)
(* Of an operator in the basis basb *)
topb[ls_,Nbi_,Ji_,Nbf_,Jf_,top_]:=
Block[{bni,dni,bnf,dnf},
bni=basb[ls,Nbi,Ji]; dni=Length[bni];
bnf=basb[ls,Nbf,Jf]; dnf=Length[bnf];
Table[top[ls,bnf[[ib]],bni[[ik]]],{ib,1,dnf},{ik,1,dni}]];
topb[ls_,ps_,Nbi_,Ji_,Pi_,Nbf_,Jf_,Pf_,top_]:=
Block[{bni,dni,bnf,dnf},
bni=basb[ls,ps,Nbi,Ji,Pi]; dni=Length[bni];
bnf=basb[ls,ps,Nbf,Jf,Pf]; dnf=Length[bnf];
Table[top[ls,bnf[[ib]],bni[[ik]]],{ib,1,dnf},{ik,1,dni}]];

(* Of an operator between eigenstates of hops+ints *)
tranb[ls_,Nbi_,Ji_,ni_,Nbf_,Jf_,nf_,hops_,ints_,top_]:=
Block[{eigi,eni,wai,eigf,enf,waf,tmat,trans},
eigi=eigensystem[sqh[hopvb[ls,Nbi,Ji,hops,ints]]];
eni=Take[eigi[[1]],ni]; wai=Take[eigi[[2]],ni];
eigf=eigensystem[sqh[hopvb[ls,Nbf,Jf,hops,ints]]];
enf=Take[eigf[[1]],nf]; waf=Take[eigf[[2]],nf];
tmat=topb[ls,Nbi,Ji,Nbf,Jf,top];
trans=Simplify[waf . tmat . Transpose[wai]];
Do[Print["<",Jf,"(",if,";",enf[[if]],")||",top,"||",
         Ji,"(",ii,";",eni[[ii]],")>=",trans[[if,ii]]],
   {if,1,nf},{ii,1,ni}]];
tranb[ls_,ps_,Nbi_,Ji_,Pi_,ni_,Nbf_,Jf_,Pf_,nf_,hops_,ints_,top_]:=
Block[{sPi,eigi,eni,wai,sPf,eigf,enf,waf,tmat,trans},
sPi=Which[Pi==1,"+",Pi==-1,"-"]; sPf=Which[Pf==1,"+",Pf==-1,"-"]; 
eigi=eigensystem[sqh[hopvb[ls,ps,Nbi,Ji,Pi,hops,ints]]];
eni=Take[eigi[[1]],ni]; wai=Take[eigi[[2]],ni];
eigf=eigensystem[sqh[hopvb[ls,ps,Nbf,Jf,Jf,hops,ints]]];
enf=Take[eigf[[1]],nf]; waf=Take[eigf[[2]],nf];
tmat=topb[ls,ps,Nbi,Ji,Pi,Nbf,Jf,Pf,top];
trans=Simplify[waf . tmat . Transpose[wai]];
Do[Print["<",Jf,sPf,"(",if,";",enf[[if]],")||",top,"||",
         Ji,sPi,"(",ii,";",eni[[ii]],")>=",trans[[if,ii]]],
   {if,1,nf},{ii,1,ni}]];

(* Electromagnetic transitions *)
(* Calculates transitions from Ji(1 to ni) to Jf(1 to nf)
   c=1: reduced transition matrix elements
   c=2: B(E or M) values
   c=3: outputs reduced matrix elements *)
emtrb[ls_,Nb_,Ji_,ni_,Jf_,nf_,hops_,ints_,top_,c_]:=
Block[{eigi,eigf,tmat,tem},
eigi=specb[ls,Nb,{Ji},ni,hops,ints,4][[1]];
eigf=specb[ls,Nb,{Jf},nf,hops,ints,4][[1]];
tmat=topb[ls,Nb,Ji,Nb,Jf,top];
tem=Chop[Simplify[eigf[[2]] . tmat . Transpose[eigi[[2]]]]];
Which[
c==1,Do[Print["<",Jf,"(",if,")||",top,"||",Ji,"(",ii,")>=",tem[[if,ii]]],
       {if,1,nf},{ii,1,ni}],
c==2,Do[Print["B[",top,";",Ji,"(",ii,")->",Jf,"(",if,")]=",tem[[if,ii]]^2/(2*Ji+1)],
       {if,1,nf},{ii,1,ni}],
c==3,tem]];
emtrb[ls_,ps_,Nb_,Ji_,Pi_,ni_,Jf_,Pf_,nf_,hops_,ints_,top_,c_]:=
Block[{sPi,eigi,sPf,eigf,tmat,tem},
sPi=Which[Pi==1,"+",Pi==-1,"-"]; sPf=Which[Pf==1,"+",Pf==-1,"-"];
eigi=specb[ls,ps,Nb,{Ji},{Pi},ni,hops,ints,4][[1]];
eigf=specb[ls,ps,Nb,{Jf},{Pf},nf,hops,ints,4][[1]];
tmat=topb[ls,ps,Nb,Ji,Pi,Nb,Jf,Pf,top];
tem=Chop[Simplify[eigf[[2]] . tmat . Transpose[eigi[[2]]]]];
Which[
c==1,Do[Print["<",Jf,sPf,"(",if,")||",top,"||",Ji,sPi,"(",ii,")>=",tem[[if,ii]]],
       {if,1,nf},{ii,1,ni}],
c==2,Do[Print["B[",top,";",Ji,sPi,"(",ii,")->",Jf,sPf,"(",if,")]=",tem[[if,ii]]^2/(2*Ji+1)],
       {if,1,nf},{ii,1,ni}],
c==3,tem]];

(* Electromagnetic moments *)
(* Calculates the moments of J(1 to n)
   c=1: reduced matrix elements
   c=2: moments
   c=3: outputs reduced matrix elements *)
emmob[ls_,Nb_,J_,n_,hops_,ints_,top_,c_]:=
Block[{eig,tmat,tem},
eig=specb[ls,Nb,{J},n,hops,ints,4][[1]];
tmat=topb[ls,Nb,J,Nb,J,top];
tem=Chop[Simplify[eig[[2]] . tmat . Transpose[eig[[2]]]]];
Which[
c==1,Do[Print["<",J,"(",i,")||",top,"||",J,"(",i,")>=",tem[[i,i]]],{i,1,n}],
c==2 && top===obM1,Do[Print["\[Mu][",J,"(",i,")]=",Sqrt[4*Pi/3]*wigner[J,1,J,-J,0,J]*tem[[i,i]]],{i,1,n}],
c==2 && top===obE2,Do[Print["Q[",J,"(",i,")]=",Sqrt[16*Pi/5]*wigner[J,2,J,-J,0,J]*tem[[i,i]]],{i,1,n}],
c==3,tem]];
emmob[ls_,ps_,Nb_,J_,P_,n_,hops_,ints_,top_,c_]:=
Block[{sP,eig,tmat,tem},
sP=Which[P==1,"+",P==-1,"-"];
eig=specb[ls,ps,Nb,{J},{P},n,hops,ints,4][[1]];
tmat=topb[ls,ps,Nb,J,P,Nb,J,P,top];
tem=Chop[Simplify[eig[[2]] . tmat . Transpose[eig[[2]]]]];
Which[
c==1,Do[Print["<",J,sP,"(",i,")||",top,"||",J,sP,"(",i,")>=",tem[[i,i]]],{i,1,n}],
c==2 && top==obM1,Do[Print["\[Mu][",J,sP,"(",i,")]=",Sqrt[4*Pi/3]*wigner[J,1,J,-J,0,J]*tem[[i,i]]],{i,1,n}],
c==2 && top==obE2,Do[Print["Q[",J,sP,"(",i,")]=",Sqrt[16*Pi/5]*wigner[J,2,J,-J,0,J]*tem[[i,i]]],{i,1,n}],
c==3,tem]];

(** Reduced matrix element of a boson creation or annihilation operator **)
(* Reduced matrix element of b(lc)+ *)
rmebcb[ls_,ic_,bra_,ket_]:=0/; bra[[ic,1]]==0;
rmebcb[ls_,ic_,bra_,ket_]:=0/; bra[[All,1]]-Table[delta[i,ic],{i,1,Length[ls]}]!=ket[[All,1]];
rmebcb[ls_,ic_,bra_,ket_]:=
(* rmebcb[ls,ic,bra,ket]= *)
Block[{p,lc,topc,topa},
p=Length[ls]; lc=ls[[ic]]; 
topc=Table[Which[i<ic,{0,0,1,0,0},i==ic,{1,1,1,lc,lc},i>ic,{0,0,1,0,lc}],{i,1,p}];
topa=Table[{0,0,1,0,0},{i,1,p}];
tmeb[ls,{topc,topa,lc},bra,ket]];
(* Reduced matrix element of b(la) *)
rmebab[ls_,ia_,bra_,ket_]:=0/; ket[[ia,1]]==0;
rmebab[ls_,ia_,bra_,ket_]:=0/; bra[[All,1]]!=ket[[All,1]]-Table[delta[i,ia],{i,1,Length[b1j]}];
rmebab[ls_,ia_,bra_,ket_]:=
(* rmebab[ls,ia,bra,ket]= *)
Block[{p,la,topc,topa},
p=Length[ls]; la=ls[[ia]]; 
topc=Table[{0,0,1,0,0},{i,1,p}];
topa=Table[Which[i<ia,{0,0,1,0,0},i==ia,{1,1,1,la,la},i>ia,{0,0,1,0,la}],{i,1,p}];
tmeb[ls,{topc,topa,la},bra,ket]];

(** Reduced matrix element of a combination of bilinear operators T^(lambda)[top] **)
(* tops contains the sequence {top1,top2,...} where top defines the operator
   T^(lambda)[top] = \sum_{lc,la} acb^(top,lambda,ic,ia) [b+(ic) b(ia)]^(lambda),
   where the coefficients acb^(top,lambda,ic,ia) are predefined;
   lambdas contains the sequence {lambda1,lambda2,...};
   Lambdas contains the sequence {Lambda1=lambda1,Lambda2,...,Lambdan}.
   bra={{n1b,v1b,a1b,J1b,K1b},{n2b,v2b,a2b,J2b,K2b},...,{npb,vpb,apb,Jpb,Kpb}}
   ket={{n1k,v1k,a1k,J1k,K1k},{n2k,v2k,a2k,J2k,K2k},...,{npk,vpk,apk,Jpk,Kpk}} *)

(* The ac coefficients satisfy the triangular condition. *)
acb[ls_,top_,lambda_,ic_,ia_]:=0/; !trian[lambda,ls[[ic]],ls[[ia]]];

(* Some conditions for a zero reduced matrix element *)
rmeTb[ls_,tops_,lambdas_,Lambdas_,bra_,ket_]:=0/;
!trian[Lambdas[[-1]],bra[[-1,5]],ket[[-1,5]]];
rmeTb[ls_,tops_,lambdas_,Lambdas_,bra_,ket_]:=0/;
Apply[Plus,bra[[All,1]]]!=Apply[Plus,ket[[All,1]]];
rmeTb[ls_,tops_,lambdas_,Lambdas_,bra_,ket_]:=0/;
Apply[Or,Map[(#<0)&,Length[lambdas]-Abs[bra[[All,1]]-ket[[All,1]]]]];
rmeTb[ls_,tops_,lambdas_,Lambdas_,bra_,ket_]:=0/;
Apply[Or,Map[(#<0)&,2*Length[lambdas]-Abs[bra[[All,2]]-ket[[All,2]]]]];

(* Reduced matrix element of a series of T^(lambda)[r] *)
rmeTb[ls_,tops_,lambdas_,Lambdas_,bra_,ket_]:=
Block[{Nb,Jb,Jk,top,lambdak,Lambdak,Lambdak1,Tlast,ints,int,s},
Nb=Apply[Plus,bra[[All,1]]]; Jb=bra[[-1,5]]; Jk=ket[[-1,5]];
lambdak=lambdas[[-1]]; top=tops[[-1]]; Lambdak=Lambdas[[-1]]; Lambdak1=Lambdas[[-2]];
s=Sum[ints=basb[ls,Nb,Ji];
      Sum[int=ints[[i]];
          Tlast=rmeTb[ls,{top},{lambdak},{lambdak},int,ket];
          If[Tlast===0,0,
             racah[Lambdak1,lambdak,Lambdak,Jk,Jb,Ji]*Tlast*
             rmeTb[ls,Drop[tops,-1],Drop[lambdas,-1],Drop[Lambdas,-1],bra,int]],
          {i,1,Length[ints]}],
      {Ji,Max[Abs[Lambdak1-Jb],Abs[lambdak-Jk]],Min[Lambdak1+Jb,lambdak+Jk]}];
(-1)^(Jb+Jk+Lambdak)*Sqrt[2*Lambdak+1]*s]/; Length[lambdas]>1;

(* Reduced matrix element of a single T^(lambda)[r] *) 
rmeTb[ls_,tops_,lambdas_,Lambdas_,bra_,ket_]:=
Block[{p,lambda,top},
p=Length[ls]; lambda=lambdas[[1]]; top=tops[[1]];
Sum[If[acb[ls,top,lambda,ic,ia]===0,0,
       acb[ls,top,lambda,ic,ia]*rmeT1b[ls,ic,ia,lambda,bra,ket]],
       {ic,1,p},{ia,1,p}]]/; Length[lambdas]==1;

(* Reduced matrix element of (b(ic)+ b(ia))^(lambda) *)
rmeT1b[ls_,ic_,ia_,lambda_,bra_,ket_]:=0/;
bra[[All,1]]-Table[delta[i,ic],{i,1,Length[ls]}]!=
ket[[All,1]]-Table[delta[i,ia],{i,1,Length[ls]}];
rmeT1b[ls_,ic_,ia_,lambda_,bra_,ket_]:=
Block[{p,lc,la,opec,opea},
p=Length[ls]; lc=ls[[ic]]; la=ls[[ia]];
opec=Table[Which[i<ic,{0,0,1,0,0},i==ic,{1,1,1,lc,lc},i>ic,{0,0,1,0,lc}],{i,1,p}];
opea=Table[Which[i<ia,{0,0,1,0,0},i==ia,{1,1,1,la,la},i>ia,{0,0,1,0,la}],{i,1,p}];
tmeb[ls,{opec,opea,lambda},bra,ket]];

(** Matrix elements of tensor operators (hop or top) in multipole form **)
(* If the operator is a scalar(hop), the matrix element is calculated;
   otherwise (top), the reduced matrix element is calculated.
   obL is the angular momentum operator;
   obQ is a quadrupole operator;
   obO is an octupole operator;
   obH is a hexadecapole operator. *)
(* Scalar operators *)
obn1[ls_,bra_,ket_]:=delta[bra,ket]*bra[[1,1]];
obn2[ls_,bra_,ket_]:=delta[bra,ket]*bra[[2,1]];
obn3[ls_,bra_,ket_]:=delta[bra,ket]*bra[[3,1]];
obn4[ls_,bra_,ket_]:=delta[bra,ket]*bra[[4,1]];
obn1n1[ls_,bra_,ket_]:=2*delta[bra,ket]*bra[[1,1]]*bra[[1,1]];
obn1n2[ls_,bra_,ket_]:=delta[bra,ket]*bra[[1,1]]*bra[[2,1]];
obn2n2[ls_,bra_,ket_]:=2*delta[bra,ket]*bra[[2,1]]*bra[[2,1]];
obLL[ls_,bra_,ket_]:=delta[bra,ket]*bra[[-1,5]]*(bra[[-1,5]]+1);
obQQ[ls_,bra_,ket_]:=Sqrt[5]*rmeTb[ls,{obQ,obQ},{2,2},{2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obOO[ls_,bra_,ket_]:=-Sqrt[7]*rmeTb[ls,{obO,obO},{3,3},{3,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obHH[ls_,bra_,ket_]:=Sqrt[9]*rmeTb[ls,{obH,obH},{4,4},{4,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obQQQ[ls_,bra_,ket_]:=rmeTb[ls,{obQ,obQ,obQ},{2,2,2},{2,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obLQQ[ls_,bra_,ket_]:=rmeTb[ls,{obL,obQ,obQ},{1,2,2},{1,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obQLQ[ls_,bra_,ket_]:=rmeTb[ls,{obQ,obL,obQ},{2,1,2},{2,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obQQL[ls_,bra_,ket_]:=rmeTb[ls,{obQ,obQ,obL},{2,2,1},{2,1,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obQLL[ls_,bra_,ket_]:=rmeTb[ls,{obQ,obL,obL},{2,1,1},{2,1,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obLQL[ls_,bra_,ket_]:=rmeTb[ls,{obL,obQ,obL},{1,2,1},{1,1,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obLLQ[ls_,bra_,ket_]:=rmeTb[ls,{obL,obL,obQ},{1,1,2},{1,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obLLL[ls_,bra_,ket_]:=rmeTb[ls,{obL,obL,obL},{1,1,1},{1,1,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obLL0LL[ls_,bra_,ket_]:=rmeTb[ls,{obL,obL,obL,obL},{1,1,1,1},{1,0,1,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obLL1LL[ls_,bra_,ket_]:=rmeTb[ls,{obL,obL,obL,obL},{1,1,1,1},{1,1,1,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obLL2LL[ls_,bra_,ket_]:=rmeTb[ls,{obL,obL,obL,obL},{1,1,1,1},{1,2,1,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obQQ0QQ[ls_,bra_,ket_]:=rmeTb[ls,{obQ,obQ,obQ,obQ},{2,2,2,2},{2,0,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obQQ1QQ[ls_,bra_,ket_]:=rmeTb[ls,{obQ,obQ,obQ,obQ},{2,2,2,2},{2,1,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obQQ2QQ[ls_,bra_,ket_]:=rmeTb[ls,{obQ,obQ,obQ,obQ},{2,2,2,2},{2,2,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obQQ3QQ[ls_,bra_,ket_]:=rmeTb[ls,{obQ,obQ,obQ,obQ},{2,2,2,2},{2,3,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obQQ4QQ[ls_,bra_,ket_]:=rmeTb[ls,{obQ,obQ,obQ,obQ},{2,2,2,2},{2,4,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obLQ1LQ[ls_,bra_,ket_]:=rmeTb[ls,{obL,obQ,obL,obQ},{1,1,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obLQ2LQ[ls_,bra_,ket_]:=rmeTb[ls,{obL,obQ,obL,obQ},{1,2,1,2},{1,2,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
obLQ3LQ[ls_,bra_,ket_]:=rmeTb[ls,{obL,obQ,obL,obQ},{1,2,1,2},{1,3,2,0},bra,ket]/Sqrt[2*bra[[-1,5]]+1];
(* The order of the scalar operators in the generators must be declared *)
order[o_]:=1/; o==obn1 || o==obn2 || o==obn3 || o==obn4;
order[o_]:=2/; o==obn1n1 || o==obn1n2 || o==obn2n2 || o==obLL ||
               o==obQQ || o==obOO || o==obHH;
order[o_]:=3/; o==obQQQ || o==obLQQ || o==obQLQ || o==obQQL ||
               o==obQLL || o==obLQL || o==obLLQ || o==obLLL;
order[o_]:=4/; o==obQQ0QQ || o==obQQ1QQ || o==obQQ2QQ || o==obQQ3QQ || o==obQQ4QQ ||
               o==obLQ1LQ || o==obLQ2LQ || o==obLQ3LQ ||
               o==obLL0LL || o==obLL1LL || o==obLL2LL;
(* Non-scalar operators *)
