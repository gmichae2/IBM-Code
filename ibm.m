(*** Interacting boson model for (l1+l2+...+lp)^Nb bosons ***)

(** Output parity **)
Po[P_]:=Which[P==-1,"-",P==+1,"+"];

(** Dimension of basis **)
(* Two possible definitions for a single kind of boson *)
dimb1[ls_,n_,J_]:=Sum[multiplicity[ls[[1]],v,J],{v,Mod[n,2],n,2}];
dimb2[ls_,n_,J_]:=nstat1[1,{ls[[1]]},n,{J}];
(* Default *)
dimb[ls_,n_,J_]:=dimb2[ls,n,J]/; Length[ls]==1;
(* For several kinds of bosons *)
dimb[ls_,n_,J_]:=nstat[1,Map[({1,#,{#}})&,ls],n,{J}];

(** Basis construction in U(2l1+1) x U(2l2+1) x ... x U(2lp+1) **)
(* Input:   
   ls={l1,l2,...,lp}: angular momenta of the bosons;
   ps={p1,p2,...,pp}: parities of the bosons.
   => If an s boson is present it should be the first entry l1.
   Nb: total number of bosons (including the s bosons);
   J: total angular momentum the states;
   P: parity of the states. *)
(* Output:
   {{{n1,v1,a1,J1,K1}1,{n2,v2,a2,J2,K2}1,...,{np,vp,ap,Jp,Kp}1},
    {{n1,v1,a1,J1,K1}2,{n2,v2,a2,J2,K2}2,...,{np,vp,ap,Jp,Kp}2},
    ....}
   where
   {ni,vi,ai,Ji} are the labels associated with boson i;
   Ki is the intermediate coupling from Ki-1 x Ji;
   K1=J1 and Kp=J. *)

basb[ls_,n_,J_]:=
Block[{l,basis},
l=ls[[1]];
basis={};
Do[Do[basis=Append[basis,{{n,v,a,J,J}}],
      {a,1,mulv[l,v,J]}],
   {v,Mod[n,2],n,2}];
basis]/; Length[ls]==1;

basb[ls_,n_]:=
Flatten[Table[basb[ls,n,J],{J,0,n*ls[[1]]}],2]/; Length[ls]==1;

basb[ls_,Nb_,J_]:=
Block[{lsp,lsp1,np1,bp,Jp,Ip1,Ip1s},
lsp={ls[[-1]]}; lsp1=Drop[ls,-1];
Flatten[
Table[np1=Nb-np;
      bp=Map[Join[Drop[#,-1],{J}]&,basb[lsp,np]];
      Table[Jp=bp[[ip,4]];
            Ip1s=genang[{J},{Jp}];
            Table[Ip1=Ip1s[[ip1,1]];
                  Map[Join[#,{bp[[ip]]}]&,basb[lsp1,np1,Ip1]],
                  {ip1,1,Length[Ip1s]}],
            {ip,1,Length[bp]}],
      {np,0,Nb}],3]];

basb[ls_,ps_,Nb_,J_,P_]:=
Select[basb[ls,Nb,J],(Apply[Times,ps^(#[[All,1]])]==P)&];


(** List of all k-body scalar interactions **)
(* Notation: {{{n1c,v1c,a1c,L1c,I1c},{n2c,v2c,a2c,L2c,I2c},...,{npc,vpc,apc,Lpc,Ipc}},
              {{n1a,v1a,a1a,L1a,I1a},{n2a,v2a,a2a,L2a,I2a},...,{npa,vpa,apa,Lpa,Ipa}}}
             where c refers to the labels of the creation operator
                   a refers to the labels of the annihilation operator
                   k=n1c+n2c+...+npc=n1a+n2a+...+npa
             with L1c=I1c, L1a=I1a, Ipc=Ipa *)
genintAb[ls_,k_]:=
Block[{bk,dk,bkbk},
bk=Flatten[Table[basb[ls,k,Lk],{Lk,0,k*Max[ls]}],1]; dk=Length[bk];
bkbk=Flatten[Table[{bk[[i]],bk[[j]]},{i,1,dk},{j,1,dk}],1];
Which[k==0 || k==1,
      Table[{bk[[i]],bk[[i]]},{i,1,dk}],
      k>1,
      bkbk=Flatten[Table[{bk[[i]],bk[[j]]},{i,1,dk},{j,1,dk}],1];
      Select[bkbk,(#[[1,-1,-1]]==#[[2,-1,-1]])&]]];
(* Hermitian interactions *)
genintHb[ls_,k_]:=
Block[{bk,dk,bkbk},
bk=Flatten[Table[basb[ls,k,Lk],{Lk,0,k*Max[ls]}],1]; dk=Length[bk];
bkbk=Flatten[Table[{bk[[i]],bk[[j]]},{i,1,dk},{j,1,dk}],1];
Which[k==0 || k==1,
      Table[{bk[[i]],bk[[i]]},{i,1,dk}],
      k>1,
      bkbk=Flatten[Table[{bk[[i]],bk[[j]]},{i,1,dk},{j,i,dk}],1];
      Select[bkbk,(#[[1,-1,-1]]==#[[2,-1,-1]])&]]];
(* Parity-conserving interactions *)
genintAPb[ls_,ps_,k_]:=
Select[genintAb[ls,k],(Apply[Times,ps^(#[[1,All,1]])]==Apply[Times,ps^(#[[2,All,1]])])&];
(* Parity-conserving Hermitian interactions *)
genintHPb[ls_,ps_,k_]:=
Select[genintHb[ls,k],(Apply[Times,ps^(#[[1,All,1]])]==Apply[Times,ps^(#[[2,All,1]])])&];
(* Choose one as the default *)
genintb[ls_,k_]:=genintAb[ls,k];
genintb[ls_,ps_,k_]:=genintAPb[ls,ps,k];
(* Interactions in a simplified notation *)
genintsAb[ls_,k_]:=Map[vb[ls,#]&,genintAb[ls,k]];
genintsHb[ls_,k_]:=Map[vb[ls,#]&,genintHb[ls,k]];
genintsAPb[ls_,ps_,k_]:=Map[vb[ls,#]&,genintAPb[ls,ps,k]];
genintsHPb[ls_,ps_,k_]:=Map[vb[ls,#]&,genintHPb[ls,ps,k]];
genintsb[ls_,k_]:=Map[vb[ls,#]&,genintb[ls,k]];
genintsb[ls_,ps_,k_]:=Map[vb[ls,#]&,genintb[ls,ps,k]];

(** Matrix element of a scalar interaction **)
(* Matrix elements that are zero *)
(* kint is either the order of the interaction (a number)
   or it is an interaction element generated by genint;
   int is an interaction element generated by genint. *)
hmeb[ls_,kint_,bra_,ket_]:=0/; bra[[-1,5]]!=ket[[-1,5]];
(* Matrix element of a single scalar interaction term *)
hmeb[ls_,int_,bra_,ket_]:=0/; Length[int]!=0 && 
bra[[All,1]]-ket[[All,1]]!=int[[1,All,1]]-int[[2,All,1]];
hmeb[ls_,int_,bra_,ket_]:=0/; Length[int]!=0 && 
Apply[Or,Map[(#>0)&,Abs[bra[[All,2]]-ket[[All,2]]]-int[[1,All,2]]-int[[2,All,2]]]];
hmeb[ls_,int_,bra_,ket_]:=0/; Length[int]!=0 && 
Apply[Or,Map[(#>0)&,int[[1,All,1]]-bra[[All,1]]]] ||
Apply[Or,Map[(#>0)&,int[[2,All,1]]-ket[[All,1]]]];
hmeb[ls_,int_,bra_,ket_]:=0/; Length[int]!=0 && 
Apply[Plus,int[[1,All,1]]]<=1 && Apply[Plus,int[[2,All,1]]]<=1 && bra!=ket;
hmeb[ls_,int_,bra_,ket_]:=0/; Length[int]!=0 && 
Apply[Plus,int[[1,All,1]]]==Apply[Plus,bra[[All,1]]] && int[[1,-1,5]]!=bra[[-1,5]];

(* Matrix element of a k-body scalar interaction *)
hmeb[ls_,k_,bra_,ket_]:=
Block[{ints,di},
ints=genintb[ls,k]; di=Length[ints];
Sum[hmeb[ls,ints[[i]],bra,ket],{i,1,di}]]/; Length[k]==0;

(* Elimination of the s-boson dependence *)
hmeb[ls_,int_,bra_,ket_]:=
Sqrt[Binomial[bra[[1,1]],int[[1,1,1]]]*Binomial[ket[[1,1]],int[[2,1,1]]]]*
hmeb[Drop[ls,1],Map[Drop[#,1]&,int],Drop[bra,1],Drop[ket,1]]/; ls[[1]]==0;

(* Matrix element of a single scalar interaction term for a single l-boson *)
(* Notation: int={{{nc,vc,ac,Lc,Ic}},{{na,va,aa,La,Ia}}}
             with Lc=Ic=La=Ia;
             bra={{nb,vb,ab,Jb,Kb}}, ket={nk,vk,ak,Jk,Kk}
             with Jb=Kb=Jk=Kk. *)
hmeb[ls_,int_,bra_,ket_]:=
Block[{l,L,J,mc,vc,ac,ma,va,aa,nb,vb,ab,nk,vk,ak,ni,s},
l=ls[[1]]; L=int[[1,1,4]]; J=bra[[1,4]];
mc=int[[1,1,1]]; vc=int[[1,1,2]]; ac=int[[1,1,3]];
ma=int[[2,1,1]]; va=int[[2,1,2]]; aa=int[[2,1,3]];
nb=bra[[1,1]]; vb=bra[[1,2]]; ab=bra[[1,3]];
nk=ket[[1,1]]; vk=ket[[1,2]]; ak=ket[[1,3]];
ni=nb-mc;
s=Sum[cfps[l,ni,vi,ai,Li,mc,vc,ac,L,nb,vb,ab,J]*
      cfps[l,ni,vi,ai,Li,ma,va,aa,L,nk,vk,ak,J],
      {vi,Max[Abs[vb-vc],Abs[vk-va]],Min[ni,vb+vc,vk+va]},
      {Li,Abs[J-L],J+L},
      {ai,mulv[l,vi,Li]}];
redsqrtrat[Sqrt[Binomial[nb,mc]*Binomial[nk,ma]]*s]]/; Length[ls]==1;

(* General matrix element of a single scalar interaction term *)
(* Notation: int={{{n1c,v1c,a1c,L1c,I1c},{n2c,v2c,a2c,L2c,I2c},...,{npc,vpc,apc,Lpc,Ipc}},
                  {{n1a,v1a,a1a,L1a,I1a},{n2a,v2a,a2a,L2a,I2a},...,{npa,vpa,apa,Lpa,Ipa}}}
             with L1c=I1c, L1a=I1a, Ipc=Ipa;
             opep1={{{n1c,v1c,a1c,L1c,I1c},...,{np1c,vp1c,ap1c,Lp1c,Ip1c}},
                    {{n1a,v1a,a1a,L1a,I1a},...,{np1a,vp1a,ap1a,Lp1a,Ip1a}},R}
             opep={{{npc,vpc,apc,Lpc,Ipc}},{{npa,vpa,apa,Lpa,Ipa}},R}
             where c refers to the labels of the creation operator
                   a refers to the labels of the annihilation operator;
             bra={{n1b,v1b,a1b,J1b,K1b},{n2b,v2b,a2b,J2b,K2b},...,{npb,vpb,apb,Jpb,Kpb}}
             ket={{n1k,v1k,a1k,J1k,K1k},{n2k,v2k,a2k,J2k,K2k},...,{npk,vpk,apk,Jpk,Kpk}}
             with J1b=K1b, J1k=K1k, Kpb=Kpk;
             brap1={{n1b,v1b,a1b,J1b,K1b},...,{np1b,vp1b,ap1b,Jp1b,Kp1b}}
             ketp1={{n1k,v1k,a1k,J1k,K1k},...,{np1k,vp1k,ap1k,Jp1k,Kp1k}}
             brap={{npb,vpb,apb,Jpb,Kpb}}
             ketp={{npk,vpk,apk,Jpk,Kpk}}. *)
hmeb[ls_,int_,bra_,ket_]:=
Block[{lsp1,lsp,intp1,intp,opep1,opep,brap1,brap,ketp1,ketp,
       Ip1c,Ip1a,Lpc,Lpa,L,Kp1b,Kp1k,Jpb,Jpk,J,s},
lsp1=Drop[ls,-1]; intp1=Map[Drop[#,-1]&,int];
brap1=Drop[bra,-1]; ketp1=Drop[ket,-1];
lsp=Take[ls,-1];
intp=Map[{#[[-1,{1,2,3,4,4}]]}&,int];
brap={bra[[-1,{1,2,3,4,4}]]}; ketp={ket[[-1,{1,2,3,4,4}]]};
Ip1c=int[[1,-2,5]]; Ip1a=int[[2,-2,5]];
Lpc=int[[1,-1,4]]; Lpa=int[[2,-1,4]]; L=int[[1,-1,5]];
Kp1b=bra[[-2,5]]; Kp1k=ket[[-2,5]];
Jpb=bra[[-1,4]]; Jpk=ket[[-1,4]]; J=bra[[-1,5]];
s=Sum[opep1=Join[intp1,{R}]; opep=Join[intp,{R}];
      racah[Ip1c,Lpc,L,Lpa,Ip1a,R]*racah[Kp1b,Jpb,J,Jpk,Kp1k,R]*
      tmeb[lsp1,opep1,brap1,ketp1]*tmeb[lsp,opep,brap,ketp],
      {R,Max[Abs[Ip1c-Ip1a],Abs[Lpc-Lpa],Abs[Kp1b-Kp1k],Abs[Jpb-Jpk]],
         Min[Ip1c+Ip1a,Lpc+Lpa,Kp1b+Kp1k,Jpb+Jpk]}];
redsqrtrat[(-1)^(Kp1k+Jpb+Ip1a+Lpc+L+J)*(2*L+1)*s]];

(** Reduced matrix element of an operator **)
(* Reduced matrix elements that are zero *)
tmeb[ls_,ope_,bra_,ket_]:=0/; !trian[ope[[3]],bra[[-1,5]],ket[[-1,5]]];
tmeb[ls_,ope_,bra_,ket_]:=0/;
bra[[All,1]]-ket[[All,1]]!=ope[[1,All,1]]-ope[[2,All,1]];
tmeb[ls_,ope_,bra_,ket_]:=0/;
Apply[Or,Map[(#>0)&,Abs[bra[[All,2]]-ket[[All,2]]]-ope[[1,All,2]]-ope[[2,All,2]]]];
tmeb[ls_,ope_,bra_,ket_]:=0/;
Apply[Or,Map[(#>0)&,ope[[1,All,1]]-bra[[All,1]]]] ||
Apply[Or,Map[(#>0)&,ope[[2,All,1]]-ket[[All,1]]]];

(* Elimination of the s-boson dependence *)
tmeb[ls_,ope_,bra_,ket_]:=
Sqrt[Binomial[bra[[1,1]],ope[[1,1,1]]]*Binomial[ket[[1,1]],ope[[2,1,1]]]]*
tmeb[Drop[ls,1],Join[Map[Drop[#,1]&,ope[[{1,2}]]],{ope[[-1]]}],Drop[bra,1],Drop[ket,1]]/; ls[[1]]==0;

(* Reduced matrix element for a single l-boson *)
(* Notation: ope={{{nc,vc,ac,Lc,Ic}},{{na,va,aa,La,Ia}},R}
             with Lc=Ic, La=Ia;
             bra={{nb,vb,ab,Jb,Kb}}, ket={{nk,vk,ak,Jk,Kk}}
             with Jb=Kb, Jk=Kk. *)
tmeb[ls_,ope_,bra_,ket_]:=
Block[{l,R,nc,vc,ac,Lc,na,va,aa,La,nb,vb,ab,Jb,nk,vk,ak,Jk,ni,s},
l=ls[[1]]; R=ope[[3]];
nc=ope[[1,1,1]]; vc=ope[[1,1,2]]; ac=ope[[1,1,3]]; Lc=ope[[1,1,4]];
na=ope[[2,1,1]]; va=ope[[2,1,2]]; aa=ope[[2,1,3]]; La=ope[[2,1,4]];
nb=bra[[1,1]]; vb=bra[[1,2]]; ab=bra[[1,3]]; Jb=bra[[1,4]];
nk=ket[[1,1]]; vk=ket[[1,2]]; ak=ket[[1,3]]; Jk=ket[[1,4]];
ni=nb-nc;
s=Sum[(-1)^Ji*racah[Lc,La,R,Jk,Jb,Ji]*
      cfps[l,ni,vi,ai,Ji,nc,vc,ac,Lc,nb,vb,ab,Jb]*
      cfps[l,ni,vi,ai,Ji,na,va,aa,La,nk,vk,ak,Jk],
      {vi,Max[Abs[vb-vc],Abs[vk-va]],Min[ni,vb+vc,vk+va]},
      {Ji,Max[Abs[Jb-Lc],Abs[Jk-La]],Min[Jb+Lc,Jk+La]},
      {ai,mulv[l,vi,Ji]}];
redsqrtrat[(-1)^(Jb+La+R)*Sqrt[(2*Jb+1)*(2*R+1)*(2*Jk+1)*
           Binomial[nb,nc]*Binomial[nk,na]]*s]]/; Length[ls]==1;

(* General reduced matrix element *)
(* Notation: ope={{{n1c,v1c,a1c,L1c,I1c},{n2c,v2c,a2c,L2c,I2c},...,{npc,vpc,apc,Lpc,Ipc}},
