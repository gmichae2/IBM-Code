R=ope[[3]];
lsp1=Drop[ls,-1]; intp1=Map[Drop[#,-1]&,ope[[{1,2}]]];
brap1=Drop[bra,-1]; ketp1=Drop[ket,-1];
lsp=Take[ls,-1];
intp=Map[{#[[-1,{1,2,3,4,4}]]}&,ope[[{1,2}]]];
brap={bra[[-1,{1,2,3,4,4}]]}; ketp={ket[[-1,{1,2,3,4,4}]]};
Ip1c=ope[[1,-2,5]]; Ip1a=ope[[2,-2,5]];
Lpc=ope[[1,-1,4]]; Lpa=ope[[2,-1,4]]; Ipc=ope[[1,-1,5]]; Ipa=ope[[2,-1,5]];
Kp1b=bra[[-2,5]]; Kp1k=ket[[-2,5]];
Kpb=bra[[-1,5]]; Kpk=ket[[-1,5]]; Jpb=bra[[-1,4]]; Jpk=ket[[-1,4]];
s=Sum[opep1=Join[intp1,{Rp1}]; opep=Join[intp,{Rp}];
