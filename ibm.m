             brap1={{n1b,v1b,a1b,J1b,K1b},...,{np1b,vp1b,ap1b,Jp1b,Kp1b}}
             ketp1={{n1k,v1k,a1k,J1k,K1k},...,{np1k,vp1k,ap1k,Jp1k,Kp1k}}
             brap={{npb,vpb,apb,Jpb,Kpb}}
             ketp={{npk,vpk,apk,Jpk,Kpk}}. *)
tmeb[ls_,ope_,bra_,ket_]:=
