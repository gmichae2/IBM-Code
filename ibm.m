Block[{ints,di,bn,dn},
ints=genintb[ls,k]; di=Length[ints];
bn=basb[ls,Nb,J]; dn=Length[bn];
Table[Sum[vb[ls,ints[[i]]]*hmeb[ls,ints[[i]],bn[[ib]],bn[[ik]]],{i,1,di}],{ib,1,dn},{ik,1,dn}]];
hamb[ls_,k_,Nb_,J_]:=
