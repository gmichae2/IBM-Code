      Sqrt[(2*Rp1+1)*(2*Rp+1)]*
      ninej[Ip1c,Lpc,Ipc,Ip1a,Lpa,Ipa,Rp1,Rp,R]*
      ninej[Kp1b,Jpb,Kpb,Kp1k,Jpk,Kpk,Rp1,Rp,R]*
      tmeb[lsp1,opep1,brap1,ketp1]*tmeb[lsp,opep,brap,ketp],
      {Rp1,Max[Abs[Ip1c-Ip1a],Abs[Kp1b-Kp1k]],Min[Ip1c+Ip1a,Kp1b+Kp1k]},
      {Rp,Max[Abs[Lpc-Lpa],Abs[Jpb-Jpk],Abs[Rp1-R]],Min[Lpc+Lpa,Jpb+Jpk,Rp1+R]}];
